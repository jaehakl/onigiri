    i = 0
    while (i < len(kana)):
        if i < len(kana)-2:
            s3 = kana[i:i+3]
            if s3 in yo_tsuun_to_hangul:
                hangul += yo_tsuun_to_hangul[s3]
                i += 3
                continue
