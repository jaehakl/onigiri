            s2 = kana[i:i+2]
            if s2 in tsuun_to_hangul:
                hangul += tsuun_to_hangul[s2]
                i += 2
                continue
            elif s2 in yo_to_hangul:
                hangul += yo_to_hangul[s2]
                i += 2
                continue
